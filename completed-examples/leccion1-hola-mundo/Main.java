public class Main {

    public static void main(String[] args) {

        // System.out.println() imprime un mensaje y salta a la siguiente línea
        System.out.println("¡Hola Mundo! Mi primer programa en Java.");

        // Puedes imprimir lo que quieras entre las comillas
        System.out.println("Mi nombre es Ana.");

        System.out.println("Estoy en 3ro de secundaria.");

        // También puedes imprimir números directamente (sin comillas)
        System.out.println(2024);

    }
}

